let initPromise: Promise<void> | null = null;

export function getDeviceLanguage(): AppLanguage {
  const candidate = Localization.getLocales()?.[0]?.languageCode;
  const supported = SUPPORTED_LANGUAGES.find((item) => item.code === candidate);
  return supported?.code ?? FALLBACK_LANGUAGE;
}

export async function initializeI18n(language: AppLanguage = FALLBACK_LANGUAGE) {
  if (!initPromise) {
    initPromise = (async () => {
      await i18n.use(initReactI18next).init({
        compatibilityJSON: 'v4',
        resources,
        lng: language,
        fallbackLng: FALLBACK_LANGUAGE,
        interpolation: {
          escapeValue: false,
        },
        react: {
          useSuspense: false,
        },
        returnNull: false,
      });
    })();
  }

  await initPromise;

  if (i18n.language !== language) {
    await i18n.changeLanguage(language);
  }

  return i18n;
}

export async function setAppLanguage(language: AppLanguage) {
  await initializeI18n(language);
  if (i18n.language !== language) {
    await i18n.changeLanguage(language);
  }
}

export default i18n;
